type TestRunner struct{
	executions int
	stat1 []byte
	stat2 []byte
}

func (runner TestRunner) Run(cmd string, args ...string) ([]byte, error) {
	runner.executions++

	if runner.executions % 2 == 0 {
		return runner.stat1, nil
	} else {
		return runner.stat2, nil
	}
}

func TestNewCpuMon(t *testing.T) {
	runner := TestRunner{0, []byte(""), []byte("")}
	cpu := NewCpuMon(runner)

	if &cpu == nil {
		t.Fail()
	}
}

